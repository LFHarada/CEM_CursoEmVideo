# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

num = float(input('Digite um número: '))
ant = num - 1
suc = num + 1

print('O número que você escolheu é: {:.2f}'.format(num))
print('O antecessor do número que você escolheu é: {:.2f}'.format(ant))
print('O sucessor número que você escolheu é: {:.2f}'.format(suc))
print('O sucessor número que você escolheu é: {:.2f}'.format(suc))